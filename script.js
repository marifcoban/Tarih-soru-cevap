function populateAchievements(category) {
    var achievementsContainer = document.getElementById("achievements-container");
    achievementsContainer.innerHTML = ""; // Önceki içeriği temizle

     var categoryAchievements = categoriesAchievements[category];
    if (!categoryAchievements) {
        return; // Kategori için kazanım tanımı yoksa işlemi sonlandır
    } 

    categoryAchievements.forEach(function (achievement) {
        var checkbox = document.createElement("input");
        checkbox.type = "checkbox";
        checkbox.name = "achievements";
        checkbox.value = achievement;
        checkbox.id = "achievement-" + achievement.toLowerCase().replace(/\s+/g, "-");
        
        var label = document.createElement("label");
        label.htmlFor = checkbox.id;
        label.appendChild(document.createTextNode(achievement));

        achievementsContainer.appendChild(checkbox);
        achievementsContainer.appendChild(label);
        achievementsContainer.appendChild(document.createElement("br"));
    });

    document.getElementById("achievement-selection").style.display = "block";
}

